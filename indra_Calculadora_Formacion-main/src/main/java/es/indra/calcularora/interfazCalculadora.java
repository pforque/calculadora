    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        if(txtResult.getText().contains("-")){
            String[] split=txtResult.getText().split("-");
            String part = split[1];
            num2 = Double.parseDouble(part);
            result = instancia.restar(num1,num2);
            txtResult.setText(result+"");
        }
        else if(txtResult.getText().contains("/")){
            String[] split=txtResult.getText().split("/");
            String part = split[1];
            num2 = Double.parseDouble(part);
            result = instancia.dividir(num1,num2);
            txtResult.setText(result+"");
        }
        else if(txtResult.getText().contains("*")){
            String[] split=txtResult.getText().split("\\*");
            String part = split[1];
            num2 = Double.parseDouble(part);
            result = instancia.multiplicar(num1,num2);
            txtResult.setText(result+"");
        }
        else if(txtResult.getText().contains("+")){
            String[] split=txtResult.getText().split("\\+");
            String part = split[1];
            num2 = Double.parseDouble(part);
            result = instancia.sumar(num1,num2);
            txtResult.setText(result+"");
        }
        else{
            
        }
        if(txtResult.getText().endsWith(".0")){
            int redondeado = (int) (Math.round(result * 100) / 100d);
            txtResult.setText(redondeado+"");
        }
        comprobacion = true;
    }//GEN-LAST:event_btnIgualActionPerformed

